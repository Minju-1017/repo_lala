package com.lala.fapp.contact.affiliation;

public class ContactAffiliationDto {
    
	private String seq;
	private int main;
	private String company;
	private String department;
	private String position;
	private String contact_seq;
	
	public String getSeq() {
		return seq;
	}
	
	public void setSeq(String seq) {
		this.seq = seq;
	}
	
	public int getMain() {
		return main;
	}
	
	public void setMain(int main) {
		this.main = main;
	}
	
	public String getCompany() {
		return company;
	}
	
	public void setCompany(String company) {
		this.company = company;
	}
	
	public String getDepartment() {
		return department;
	}
	
	public void setDepartment(String department) {
		this.department = department;
	}
	
	public String getPosition() {
		return position;
	}
	
	public void setPosition(String position) {
		this.position = position;
	}
	
	public String getContact_seq() {
		return contact_seq;
	}
	
	public void setContact_seq(String contact_seq) {
		this.contact_seq = contact_seq;
	}
	
}
